/**
 * Архитектура разбора характеристик по форматам поставки (ТЗ / ЕИС).
 *
 * Type A — «colon»: строки вида «Наименование: значение» (текущий безопасный baseline).
 * Type B — «simple_table»: две колонки в одной строке, разделитель TAB (редкий экспорт).
 * Type C — «eis_wide_table»: печатная форма ЕИС, каждая ячейка — отдельная строка; есть
 *   колонка инструкций, отдельные поля позиции и блок характеристик.
 *
 * Правило внедрения: новый структурный формат → новый enum + новый файл-парсер + детектор
 * с жёсткими порогами; при сомнении всегда fallback на Type A (colon).
 *
 * Контракт парсера (общий):
 * - В characteristics попадают только пары «логическое имя характеристики» / «значение для закупки».
 * - Общие поля позиции (наименование товара в шапке таблицы, код ОКПД2 как метка позиции,
 *   назначение позиции, количество закупки и т.п.) — не characteristics.
 * - Служебные фразы ЕИС (инструкции участнику) — не characteristics.
 * - Единица измерения товара / количество товара (позиция) — не characteristics
 *   (количество позиции извлекается отдельно, не здесь).
 */

export enum CharacteristicsFormat {
  /** Type A: безопасный default и fallback при неуверенном детекте. */
  Colon = "colon",
  /** Type B: TSV-строки в теле блока. */
  SimpleTable = "simple_table",
  /** Type C: wide-table ЕИС (ячейки построчно + служебная колонка). */
  EisWideTable = "eis_wide_table",
}
